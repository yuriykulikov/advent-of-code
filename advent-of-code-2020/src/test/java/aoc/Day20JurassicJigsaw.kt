        )
            .edges()

        assertThat(edges).contains("..##.#..#.")
        assertThat(edges).contains("..##.#..#.".reversed())
        assertThat(edges).contains("..###..###")
        assertThat(edges).contains("..###..###".reversed())
        assertThat(edges).contains(".#####..#.")
        assertThat(edges).contains(".#####..#.".reversed())
        assertThat(edges).contains("...#.##..#")
        assertThat(edges).contains("...#.##..#".reversed())
    }

    companion object {
        fun Map<Point, Char>.edges(): List<String> {
            return listOf(
                (0..9).joinToString("") { x -> getValue(Point(x, 0)).toString() },
                (0..9).joinToString("") { x -> getValue(Point(x, 9)).toString() },
                (0..9).joinToString("") { y -> getValue(Point(0, y)).toString() },
                (0..9).joinToString("") { y -> getValue(Point(9, y)).toString() },
            ).flatMap {
                listOf(it, it.reversed())
            }
        }
    }

    class Image(
        private val input: String,
    ) {
        val tiles = input.split("\n\n")
            .map { it.removePrefix("Tile ").substringBefore(":").toInt() to parseMap(it.substringAfter(":\n")) }
            .toMap()

        val idToEdges: Map<Int, List<String>> = tiles
            .map { (id, map) -> id to map.edges() }
            .toMap()

        val edgeToIds: Map<String, List<Int>> =
            idToEdges.entries.flatMap { (id, edges) -> edges.map { edge -> edge to id } }
                .groupBy(keySelector = { (edge, _) -> edge }, valueTransform = { (edge, id) -> id })

        val idToNeighbors = idToEdges.keys.map { id ->
            val neighbors = idToEdges.getValue(id).flatMap { edge -> edgeToIds.getValue(edge) }.distinct().minus(id)
            id to neighbors
        }
            .toMap()
    }

    private fun cornerProduct(input: String): Long {
        // 1951    2311    3079
        // 2729    1427    2473
        // 2971    1489    1171

        return Image(input).idToNeighbors.filterValues { it.size == 2 }
            .keys
            .map { it.toLong() }
            .reduce(Long::times)
    }

